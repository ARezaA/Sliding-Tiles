<h3>About</h3>
<br>

<p>
  <strong>Dundee Corporation</strong>
</p>
<p>
    Dundee Corporation is a Canadian independent publicly traded asset management company listed on the Toronto Stock Exchange under the symbol "DC.A". Asset
    management activities are focused in the areas of the Corporation's core competencies and include resources, real estate and infrastructure, and more
    recently, the agricultural sector. These activities are carried out by the Corporation's wholly owned subsidiary, Goodman &amp; Company, Investment Counsel
    Inc. Dundee's capital markets and personal investment advisory services are carried out through the "Dundee Capital Markets" and "Dundee Goodman Private
    Wealth" divisions of its wholly owned subsidiary, Dundee Securities Ltd. Dundee also owns and manages direct investments in its core focus areas and other
    select investments, through ownership, directly and indirectly, of both publicly listed and private companies.
</p>
<p>
  <strong>Methodology</strong>
</p>
<p>
    Dundee operates with a disciplined intrinsic value-oriented investment methodology, a proprietary growth-momentum style of investing and focus investing.
    We stress the importance of anticipating, and responding to, new global investment patterns. Our working environment is characterized by creativity,
    lateral thinking, a unique collegial atmosphere and long-term loyalty commitments with key personnel.
</p>
<p>
  <strong>Objective</strong>
</p>
<p>
    Management's objective is to achieve outstanding long-term growth per share value. To meet this objective, we strive to manage our own business well and to
    invest, on behalf of others and for our own corporate portfolio, in growing businesses which can earn above-average returns on investment capital.
</p>
<p>
  <strong>Code of Ethics</strong>
</p>
<p>
    As professional investment managers and Financial advisors, we abide by three sets of rules. The first set consists of the laws and regulations adopted by
    governments, securities commissions and the Investment Industry Regulatory Organization of Canada. The second set of rules resides in the Code of Ethics
    and Standards of Professional Conduct of the CFA Institute. The government, regulatory and CFA Institute rules are minimum standards.
</p>


