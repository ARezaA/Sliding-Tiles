<h3>Accessibility</h3>
<br>

<p>
    <strong>Accessibility Policy</strong>
</p>
<p>
    Dundee Corporation and its affiliates, including Dundee Global Investment Management, Goodman &amp; Company, Investment Counsel Inc, Dundee Securities
    Ltd., and its divisions Dundee Goodman Private Wealth and Dundee Capital Markets ("Dundee"), are committed to providing clients with the highest standard
    of service possible. This includes providing accessible service to all clients in a manner that respects the dignity and independence of people with
    disabilities and complying with the mandatory standards addressed in Ontario Regulation 429/07 under the Accessibility for Ontarians with Disabilities Act,
    2005.
</p>
<p>
    In the event of a planned or unexpected disruption of services to clients with disabilities at any Dundee location, clients will be notified by posting the
    details (i) on our website at this tab, and (ii) at the location itself in a conspicuous place at the point of disruption, the main entrance and/or the
    nearest accessible entrance to the service disruption. The notification will include information about the reason for the disruption, its anticipated
    length of time, and a description of alternative facilities, if available, as well as contact information for our Accessibility Officer.
</p>
<p>
    <strong>General Principles</strong>
</p>
<p>
    <strong>Training</strong>
    : Dundee will provide accessibility training to employees who deal with the public or who are involved in the development and approval of customer service
    policies, practices and procedures. The amount and format of training given will be tailored to suit the employee's level of interaction with clients and
    employees are trained within 90 days of their start date and when changes occur to our Accessibility Policy.
</p>
<p>
    <strong>Assistive Devices</strong>
    : Dundee staff are trained and familiar with various assistive devices that may be used by clients with disabilities while accessing our services. It
    should be noted that it is the responsibility of the person with a disability to ensure that his or her assistive device is operated in a safe and
    controlled manner at all times.
</p>
<p>
    <strong>Service Animals: </strong>
    Dundee welcomes people with disabilities and their service animals. Service animals are allowed on the parts of our premises that are open to the public.
    It should be noted that it is the responsibility of the person with a disability to ensure that his or her service animal is kept in control at all times.
</p>
<p>
    <strong>Support Persons:</strong>
    A person with a disability who is accompanied by a support person will be allowed to have that person accompany them on our premises. In situations where
    confidential or personal information might be discussed, consent will be obtained from the client, prior to any conversation taking place in the presence
    of the support person.
</p>
<p>
    <strong>Availability of Documents</strong>
</p>
