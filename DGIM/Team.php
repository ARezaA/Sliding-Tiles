<p>
  David left an early career as a litigator in 1994 to become a Partner, Vice President and portfolio manager at the investment management firm behind
  Dynamic Funds. He became President and Chief Executive Officer of Dynamic Funds in 2001 and of DundeeWealth, Dynamic’s public company parent, in 2007.
  Under David’s leadership the firm became one of Canada’s best performing and fastest growing investment managers, was recognized as Fund Company of the
  Year a record 7 times at the Canadian Investment Awards, while growing assets under management from $5 billion to over $50 billion.
</p>
<p>
  Scotiabank’s acquisition of DundeeWealth in 2011 brought David to the Bank to lead the Dynamic fund family and Scotia Asset Management in a new role as the
  head of Global Asset Management.
</p>
<p>
  In 2014 David launched The D2 Financial Corporation to create a platform of alternative investments for retail, high-net-worth and institutional clients.
  The strategies behind D2 have been brought into Dundee Corporation with David’s acceptance of the position of President and Chief Executive Officer in July
  2014.
</p>
<p>
  Born in Montreal, Mr. Goodman received a Bachelor of Commerce degree from McGill University in 1985 and a Bachelor of Law (Cum Laude) from the University
  of Ottawa in 1988, where he now serves on the President’s Council. His Chartered Financial Analyst designation was conferred by the University of Virginia
  in 1996.
</p>
<p>
  Together with his wife Bonnie, Mr. Goodman has four children: Sarah, Ellie, Eden and Anthony. He is a member of the Board of Dundee Corporation and
  SickKids Foundation. Mr. Goodman is also a stand-up comedian affiliated with Yuk Yuks Comedy Clubs. David’s passion for performing stand-up comedy extends
  into his philanthropic activities. Mr. Goodman is the founder of the David Goodman Youth Community Trust which runs an annual comedic fundraising event
  called Humour Me and has to date raised over $2.5 million in support of youth at risk.
</p>
